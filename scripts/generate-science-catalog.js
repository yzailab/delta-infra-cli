#!/usr/bin/env node

"use strict";

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const SOURCE = path.join(ROOT, "skills", "science-tools.json");
const CHECK = process.argv.includes("--check");

function fail(message) {
  throw new Error(`science catalog: ${message}`);
}

function readCatalog() {
  const catalog = JSON.parse(fs.readFileSync(SOURCE, "utf8"));
  if (catalog.schema_version !== 1) fail("unsupported schema_version");
  if (!Array.isArray(catalog.base_skills) || !Array.isArray(catalog.tools)) {
    fail("base_skills and tools must be arrays");
  }

  const names = new Set();
  const skills = new Set(catalog.base_skills);
  for (const tool of catalog.tools) {
    if (!tool || typeof tool.name !== "string" || !tool.name) fail("tool.name is required");
    if (names.has(tool.name)) fail(`duplicate tool ${tool.name}`);
    names.add(tool.name);
    if (typeof tool.skill !== "string" || !tool.skill) fail(`tool.skill is required for ${tool.name}`);
    if (typeof tool.legacy_tool !== "string" || !tool.legacy_tool) {
      fail(`tool.legacy_tool is required for ${tool.name}`);
    }
    if (!tool.operations || typeof tool.operations !== "object" || Array.isArray(tool.operations)) {
      fail(`tool.operations must be an object for ${tool.name}`);
    }
    skills.add(tool.skill);
    for (const [operation, legacy] of Object.entries(tool.operations)) {
      if (!operation || typeof legacy !== "string" || !legacy) {
        fail(`invalid operation mapping for ${tool.name}`);
      }
    }
  }
  for (const skill of catalog.extra_skills || []) skills.add(skill);
  for (const skill of skills) {
    if (!fs.existsSync(path.join(ROOT, "skills", skill, "SKILL.md"))) {
      fail(`missing skills/${skill}/SKILL.md`);
    }
  }
  return catalog;
}

function quoted(value) {
  return JSON.stringify(value);
}

function goMapEntries(entries, indent) {
  const tokens = entries.map(([key, value]) => [`${quoted(key)}:`, quoted(value)]);
  const width = Math.max(0, ...tokens.map(([key]) => key.length));
  return tokens.map(([key, value]) => `${indent}${key.padEnd(width + 1)}${value},`);
}

function renderGo(catalog) {
  const aliases = catalog.tools.filter((tool) => tool.legacy_tool !== tool.name);
  const lines = [
    "// Code generated by scripts/generate-science-catalog.js; DO NOT EDIT.",
    "",
    "package sciencecatalog",
    "",
    "var legacyToolNames = map[string]string{",
  ];
  lines.push(...goMapEntries(aliases.map((tool) => [tool.name, tool.legacy_tool]), "\t"));
  lines.push("}", "", "var canonicalToolNames = map[string]string{");
  lines.push(...goMapEntries(aliases.map((tool) => [tool.legacy_tool, tool.name]), "\t"));
  lines.push("}", "", "var legacyEndpoints = map[string]map[string]string{");
  for (const tool of catalog.tools) {
    lines.push(`\t${quoted(tool.name)}: {`);
    lines.push(...goMapEntries(Object.entries(tool.operations), "\t\t"));
    lines.push("\t},");
  }
  lines.push("}", "");
  return lines.join("\n");
}

function renderPython(catalog) {
  const aliases = catalog.tools.filter((tool) => tool.legacy_tool !== tool.name);
  const lines = [
    '"""Generated Science catalog aliases. Do not edit by hand."""',
    "",
    "LEGACY_TOOL_ALIASES = {",
  ];
  for (const tool of aliases) lines.push(`    ${quoted(tool.name)}: ${quoted(tool.legacy_tool)},`);
  lines.push("}", "", "LEGACY_ENDPOINTS = {");
  for (const tool of catalog.tools) {
    lines.push(`    ${quoted(tool.name)}: {`);
    for (const [operation, legacy] of Object.entries(tool.operations)) {
      lines.push(`        ${quoted(operation)}: ${quoted(legacy)},`);
    }
    lines.push("    },");
  }
  lines.push("}", "");
  return lines.join("\n");
}

function skillNames(catalog) {
  return [
    ...catalog.base_skills,
    ...catalog.tools.map((tool) => tool.skill),
    ...(catalog.extra_skills || []),
  ];
}

function renderManifest(catalog) {
  return `${JSON.stringify({ skills: skillNames(catalog) }, null, 2)}\n`;
}

function renderReference(catalog) {
  const lines = [
    "# Science catalog（自动生成）",
    "",
    "本文件由 `skills/science-tools.json` 生成；不要手工修改。规范名称供 CLI/Skill 使用，legacy 名称仅用于兼容已部署的 `/science_tool` catalog。",
    "",
    "| 规范 tool | Skill | 类别 | operation | legacy endpoint |",
    "| --- | --- | --- | --- | --- |",
  ];
  for (const tool of catalog.tools) {
    for (const [operation, legacy] of Object.entries(tool.operations)) {
      lines.push(`| ${tool.name} | ${tool.skill} | ${tool.category || "-"} | ${operation} | ${legacy} |`);
    }
  }
  lines.push("");
  return lines.join("\n");
}

function renderPackage(catalog) {
  const packagePath = path.join(ROOT, "package.json");
  const pkg = JSON.parse(fs.readFileSync(packagePath, "utf8"));
  pkg.files = [
    "checksums.txt",
    "scripts/install.js",
    "scripts/install-wizard.js",
    "scripts/run.js",
    "skills/manifest.json",
    "skills/science-tools.json",
    ...skillNames(catalog).map((name) => `skills/${name}/`),
  ];
  return `${JSON.stringify(pkg, null, 2)}\n`;
}

function writeOrCheck(relativePath, content) {
  const destination = path.join(ROOT, relativePath);
  if (CHECK) {
    const current = fs.existsSync(destination) ? fs.readFileSync(destination, "utf8") : "";
    if (current !== content) fail(`${relativePath} is stale; run node scripts/generate-science-catalog.js`);
    return;
  }
  fs.mkdirSync(path.dirname(destination), { recursive: true });
  fs.writeFileSync(destination, content, "utf8");
}

const catalog = readCatalog();
writeOrCheck("internal/sciencecatalog/generated_catalog.go", renderGo(catalog));
writeOrCheck("skills/delta-science/scripts/generated_catalog.py", renderPython(catalog));
writeOrCheck("skills/manifest.json", renderManifest(catalog));
writeOrCheck("skills/delta-science/references/generated-catalog.md", renderReference(catalog));
writeOrCheck("package.json", renderPackage(catalog));

if (!CHECK) process.stdout.write("Science catalog generated.\n");
